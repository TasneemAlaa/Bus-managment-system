    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        char[] password1=jPasswordField1.getPassword();
        char[] passwordold= jPasswordField2.getPassword();
        char[] password2=jPasswordField3.getPassword();
        String finpass1=String.copyValueOf(password1);
        String finpass2=String.copyValueOf(password2);
        String passold=String.copyValueOf(passwordold);
      try{
        if(pas.ManagerPassword(acc, passold, finpass1, finpass2)== true) {
           JOptionPane.showMessageDialog(this,"Password changed");
            jPanel4.setVisible(false);
            
           jPanel2.setVisible(true);
           jPanel8.setVisible(false);
            jPanel10.setVisible(false);
        
        }
           else
           throw new Exception();
       }
      catch(Exception i){
          JOptionPane.showMessageDialog(this,"Invalid Passowrd, Enter another one");
      }
       
    }//GEN-LAST:event_jButton6ActionPerformed

