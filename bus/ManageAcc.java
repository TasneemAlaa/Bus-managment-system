        String t2=jTextField4.getText();
       try{
        if(pas.ManagerEmail(acc, t2, t1)==true){
            JOptionPane.showMessageDialog(this, "A verification link is sent to your email account.");
             jPanel4.setVisible(false);
            
           jPanel2.setVisible(true);
           jPanel8.setVisible(false);
            jPanel10.setVisible(false);
        }
       
      
        
        else{
           throw new EmailException();
        }
       }
